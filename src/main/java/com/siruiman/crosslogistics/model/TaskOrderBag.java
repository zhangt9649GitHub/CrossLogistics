package com.siruiman.crosslogistics.model;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@ApiModel(description="郭阳")
public class TaskOrderBag {
    @ApiModelProperty(value = "货袋id", example = "1")
    private Integer bagId;
    @ApiModelProperty(value = "货袋编号", example = "bagNumber")
    private String bagNumber;
    @ApiModelProperty(value = "长", example = "0.00")
    private Float length;
    @ApiModelProperty(value = "宽", example = "0.00")
    private Float width;
    @ApiModelProperty(value = "高", example = "0.00")
    private Float high;
    @ApiModelProperty(value = "载重量", example = "0.00")
    private Float load;

    public Integer getBagId() {
        return bagId;
    }

    public void setBagId(Integer bagId) {
        this.bagId = bagId;
    }

    public String getBagNumber() {
        return bagNumber;
    }

    public void setBagNumber(String bagNumber) {
        this.bagNumber = bagNumber;
    }

    public Float getLength() {
        return length;
    }

    public void setLength(Float length) {
        this.length = length;
    }

    public Float getWidth() {
        return width;
    }

    public void setWidth(Float width) {
        this.width = width;
    }

    public Float getHigh() {
        return high;
    }

    public void setHigh(Float high) {
        this.high = high;
    }

    public Float getLoad() {
        return load;
    }

    public void setLoad(Float load) {
        this.load = load;
    }

    @Override
    public String toString() {
        return "TaskOrderBag{" +
                "bagId=" + bagId +
                ", bagNumber='" + bagNumber + '\'' +
                ", length=" + length +
                ", width=" + width +
                ", high=" + high +
                ", load=" + load +
                '}';
    }
}
