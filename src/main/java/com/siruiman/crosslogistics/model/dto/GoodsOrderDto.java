package com.siruiman.crosslogistics.model.dto;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@ApiModel(description = "郝腾")
public class GoodsOrderDto {

    @ApiModelProperty(value = "骑手真实姓名", example = "sendPeople")
    private String sendPeople;
    @ApiModelProperty(value = "开始时间", example = "startDate")
    private String startDate;
    @ApiModelProperty(value = "结束时间", example = "endDate")
    private String endDate;

    public String getSendPeople() {
        return sendPeople;
    }

    public void setSendPeople(String sendPeople) {
        this.sendPeople = sendPeople;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }
}
